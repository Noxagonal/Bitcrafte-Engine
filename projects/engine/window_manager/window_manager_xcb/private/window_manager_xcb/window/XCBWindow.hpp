	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	void													Update();

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	virtual const WindowManagerPlatformHandlesBase		*	GetPlatformSpecificHandles() const override;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	const WindowManagerXCBPlatformHandles				*	GetXLibPlatformHandles() const;

